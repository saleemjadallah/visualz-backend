def test_node_bridge_script():
    """Test if Node.js bridge script exists and is executable"""
    print("\nğŸ“œ Testing Node.js Bridge Script")
    print("=" * 50)
    
    import subprocess
    from pathlib import Path
    
    try:
        # Check Node.js version
        result = subprocess.run(['node', '--version'], capture_output=True, text=True)
        if result.returncode == 0:
            print(f"âœ… Node.js available: {result.stdout.strip()}")
        else:
            print("âŒ Node.js not available")
            return
        
        # Check bridge script
        script_path = Path(__file__).parent / "parametric-furniture" / "templates" / "node-bridge.js"
        if script_path.exists():
            print(f"âœ… Bridge script found: {script_path}")
            
            # Test basic execution
            test_input = json.dumps({
                "template_type": "furniture",
                "parameters": {"culture": "modern", "eventType": "test"}
            })
            
            result = subprocess.run([
                'node', str(script_path), test_input
            ], capture_output=True, text=True, timeout=10)
            
            if result.returncode == 0:
                print("âœ… Bridge script executable")
                try:
                    output = json.loads(result.stdout)
                    print(f"   ğŸ“¦ Test output: {output.get('template_used', 'unknown')}")
                except:
                    print("   âš ï¸ Output not JSON formatted")
            else:
                print(f"âŒ Bridge script failed: {result.stderr}")
                
        else:
            print(f"âŒ Bridge script not found: {script_path}")
            
    except Exception as e:
        print(f"âŒ Node.js bridge test failed: {str(e)}")

